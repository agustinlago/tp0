package pruebas;

import java.util.*;

public class Materia {
	private int id;
	private String title;
	private String description;
	private ArrayList<Nota> grades;
	
	
	public int getId() {
		return id;
	}
	public String getTitle() {
		return title;
	}
	public String getDescription() {
		return description;
	}
	public ArrayList<Nota> getGrades() {
		return grades;
	}
	
}
