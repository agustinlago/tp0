package prueba;

public class Nota {
	private int id;
	private String value;
	private String created_at;
	private String update_at;
	
	
	public int getId() {
		return id;
	}
	public String getValue() {
		return value;
	}
	public String getCreated_at() {
		return created_at;
	}
	public String getUpdate_at() {
		return update_at;
	}
	
}
